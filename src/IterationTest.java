public class IterationTest {

    public static void main(String[] args) {

        for (int i = 0; i < (3 / 2); i++) {
            System.out.println("Test 1 i: " + i);
        }
        for (int i = 0; i < 1.5; i++) {
            System.out.println("Test 2 i: " + i);
        }
        for (int i = 0; i < Math.sqrt(2); i++) {
            System.out.println("Test 3 i: " + i);
        }
    }
}
